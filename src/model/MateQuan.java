package model;

public class MateQuan {

    private Materials m;
    private Double b_quan;
    private Double rm_quan;
    private Boolean enough;

    public Materials getM() {
        return m;
    }
    public void setM(Materials m) {
        this.m = m;
    }
    public Double getB_quan() {
        return b_quan;
    }
    public void setB_quan(Double b_quan) {
        this.b_quan = b_quan;
    }
    public Double getRm_quan() {
        return rm_quan;
    }
    public void setRm_quan(Double rm_quan) {
        this.rm_quan = rm_quan;
    }
    public Boolean getEnough() {
        return enough;
    }
    public void setEnough(Boolean enough) {
        this.enough = enough;
    }






}
